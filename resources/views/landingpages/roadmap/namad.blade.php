                    شدن یکی و تغییر شکل آن باعث کشیدگی تکه‌های
                    بعدی و دفرمه شدن محصول می‌شود. مواد شیمیایی مانند تینر و لوسیون‌های پاک‌سازی
                    بر کار تأثیرات نامطلوبی از جمله رنگ رفتگی
                    و... دارند. چرم نباید در معرض طولانی‌مدت تابش مستقیم آفتاب قرا گیرد چرا که
                    چرم خشک و چروکیده می‌شودو رفع چروک از آن کار
                    بسیار سختی است. همین‌طور لکه‌های چربی و اثرات جوهر خودکار، از روی چرم
                    به‌سختی پاک می‌شوند.</p>
                  <h4 class="inner-title small">استهلاک تجهیزات</h4>
                  <p>همان‌طور که در بخش‌های قبلی نیز اشاره شده است، برای تولید محصولات چرمی از
                    انواع مختلف تجهیزات برقی استفاده می‌شود. این
                    تجهیزات در طی استفاده‌های مکرر دچار مشکل شده و نیاز به تعمیر و یا تعویض برخی
                    قطعات را خواهند داشت و با توجه به وارداتی
                    بودن اکثر این قطعات، لوازم جانبی آن‌ها یا پیدا نمی‌شوند و یا با قیمت بسیار
                    بالا عرضه می‌گردند.</p>
