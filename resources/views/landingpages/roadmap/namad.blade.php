                    طرفی بازپرداخت وام در صورتی که سوددهی کار
                    مطابق برنامه‌ریزی مشخص صورت نگیرد (مثل بی‌کیفیت بودن مواد اولیه، فروش با
                    قیمت بسیار پایین، گرانی نهاده‌ها و ... )،
