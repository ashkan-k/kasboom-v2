                <h4 class="inner-title">مالی</h4>
                <div class="text-inner type-2">
                  <h4 class="inner-title small">نوسانات قیمت</h4>
                  <p>امروزه تهیه مواد اولیه به‌صورت نقدی بوده و با چک‌های مدت‌دار خرید و فروش
                    تجهیزات و مواد اولیه انجام نمی‌شود. از طرفی کم
                    شدن توان خرید مردم، تعداد مشتری‌ها را کاهش داده است؛ از این رو پس از خرید
                    مواد اولیه، نقدینگی کاهش یافته و تا فروش محصول
