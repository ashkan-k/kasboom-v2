                    خریداران تمایل به خرید آن‌ها را دارند و از
                    مضرات آن‌ها بی‌خبرند. برای مثال اکثر بند ساعت‌های موجود در بازار با آستر چرم
                    مصنوعی برای زیبایی با مواد شیمیایی فرآوری
                    شده که در پوست‌های حساس، باعث اگزماهای شدیدی خواهد شد. در این بین
                    تولیدکنندگان داخلی جهت رقابت، مجبور به کاهش قیمت
                    محصولات خود می‌شوند که این امر سوددهی کار را پایین آورده و تولیدی‌ها را به
