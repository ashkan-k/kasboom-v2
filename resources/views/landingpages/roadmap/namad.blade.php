                    نباشند، دچار ورشکستگی خواهند شد. بنا به اظهارات
                    کارآفرینان حوزه تولید چرم در طی چند سال گذشته قیمت تمام شده محصول حدود 5
                    برابر شده درحالی که قیمت فروش، آن‌چنان تغییری
                    نکرده و در حدود 2 برابر شده است.</p>
                  <h4 class="inner-title small">مشکلات اخذ تسهیلات</h4>
                  <p>
                    برای کسانی که بودجه کافی برای شروع سرمایه‌گذاری در زمینه تولیدات چرمی را
                    ندارند، دریافت تسهیلات بانکی یکی از راه‌های
                    فائق آمدن بر این مشکل است. گرفتن وام خود مشکلاتی را به همراه دارد. اخذ
                    تسهیلات مراحل اداری زیاد و زمان‌بری داشته و تأمین
                    ضمانتنامه برای گرفتن این تسهیلات مشکل است. در زمان پرداخت تسهیلات حداقل
                    معادل 130 درصد اصل تسهیلات وثیقه‌ی سفته یا حداقل
                    معادل 120 درصد آن وثیقه‌ی ملکی مطالبه می‌شود. این عوامل باعث شده، شروع کار
                    با گرفتن تسهیلات بسیار سخت باشد. بانک‌ها
                    پروسه دریافت وام را این‌گونه تشریح می‌کنند که 30 درصد کل هزینه طرح را متقاضی
                    باید داشته باشد و آن را به حساب بانک واریز
                    کند. بعد از انجام فونداسیون کار بانک 30 درصد تسهیلات را در اختیار متقاضی
                    قرار می‌دهد. بانک معمولاً دوره اجرای طرح را یک
                    سال در نظر می‌گیرد. از طرفی در کشور ما سالانه تورم زیادی وجود دارد و در برخی
                    کالاها افزایش قیمت به 40 درصد در طول یک سال
                    نیز می‌رسد. بنابراین برای اجرای مرحله دوم، سوم و نهایی طرح، بانک 30 ، 30 و
                    10 درصد تسهیلات باقی مانده را برای متقاضی در
                    طول سال آزاد می‌کند ولی به دلیل وجود تورم در طول سال، با 30 درصد یا 10 درصد
                    تسهیلات اولیه مراحل بعدی قابل اجرا نخواهند
                    بود. و در کل برای اجرای یک طرحی که مثلاً با 100 میلیون تومان در اول سال قابل
                    اجرا بود تا آخر سال بیش از 100 میلیون تومان
