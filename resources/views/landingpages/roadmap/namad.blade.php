                    نمی‌دهد و متقاضی باید بجای 30 میلیون تومان،
                    مازاد 100 میلیون تومان را نیز از خود داشته باشد. به همین جهت ورود به سیستم
                    بانکی با وجود تورم در کشور کار مشکلی است.
                    همچنین بانک‌ها بودجه مشخصی برای کارهای مختلف دارند. با توجه به تقاضای زیاد
                    جهت دریافت وام، این بودجه‌ها سریعاً تمام
                    می‌شوند و پرداخت وام برای بقیه متقاضیان به سال بعد موکول می‌شود. درکل وامی
                    که بانک پرداخت می‌کند در عمل نهایتاً 50 درصد
                    هزینه‌ها را جبران می‌کند و مابقی هزینه‌ها باید تأمین شوند. اقساط سالیانه در
                    کل 5 قسط بوده و بدون دوره‌ی تنفس می‌باشند.
