                    می‌کنند. از این رو مشکلاتی برای افراد تازه‌کار
                    پیش خواهد آمد. سود وام‌های بانکی به ظاهر با 5 درصد آغاز می‌شود ولی در نهایت
                    در عمل معمولاً زیاد بوده و 18 درصد می‌باشد.
                    در حالی که گاهاً به دلیل وضعیت نابسامان بازار، فروش، ممکن است سود کمی داشته
                    باشد. در این صورت تولیدکننده، از یک طرف 18
                    درصد سود به بانک پرداخت می‌کند و از طرفی هم تورم سالیانه و حوادث غیرقابل
                    پیش‌بینی باعث می‌شود، در نهایت سود ناچیزی برای
                    تولیدکننده باقی بماند که با توجه به حجم کار و ریسک‌های کار توجیه‌پذیر نیست.
                    همین امر باعث شده بسیاری از تولیدکنندگان،
                    کار تولید را رها کرده و به واسطه‌گری بپردازند.
